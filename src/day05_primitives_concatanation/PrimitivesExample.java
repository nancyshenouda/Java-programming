package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){
        //integer types:
        byte byteValue=100;
        short shortValue=32000;
        int intValue=344346222;
        long longValue=4586324545344434940L;
        //Decimal number/floating numbers:
        float floatValue=2345.433454F;
        double doubleValue=23456343435.565353545565;
        //Character type:
        char chValue='Y';
        //Boolean : true or false
        boolean booleanValue=true;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(chValue);
        System.out.println(booleanValue+"\n"+doubleValue);


    }
}
