package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"iphone case\",\"iphone\",\"monitor\",\"webcam\",\"speakers\",\"xbox\"");
        System.out.println("\"iphone case\",\"iphone\",\"webcam\",\"speakers\",\"monitor\",\"xbox\"");
        System.out.println("\"iphone\",\"iphone case\",\n\"webcam\",\"monitor\",\t\t\"xbox\",\t\t\"speakers\"");
        System.out.println("  ^ ");
        System.out.println("/   \\");
        System.out.println("|   |");
        System.out.println("-----");


    }
}
